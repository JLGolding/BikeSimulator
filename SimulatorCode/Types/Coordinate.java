package SimulatorCode.Types;

public class Coordinate {
    private int x = 0;
    private int y = 0;

    public int getXPosition(){
        return x;
    }

    public int getYPosition(){
        return y;
    }

    public void setX(int x){
        this.x = x;
    }

    public void setY(int y){
        this.y = y;
    }
}
